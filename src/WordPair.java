public class WordPair {
    private final String first;
    private final String second;
    public WordPair(String first, String second) {
        Object WordPair = first + second;
        this.first = first;
        this.second = second;
    }

    public String getFirst()
    {
        return first;
    }

    public String getSecond()
    {
        return second;
    }
}
